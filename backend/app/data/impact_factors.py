_UNIT_BY_CODE = {i["code"]: i["unit"] for i in EF31_INDICATORS}


def _expand() -> list[dict]:
    rows: list[dict] = []
    for ds in _DATASETS:
        missing = set(INDICATOR_CODES) - set(ds["values"])
        if missing:  # curation guard — every dataset carries all 16
            raise ValueError(f"{ds['dataset_key']} missing indicators: {missing}")
        for code in INDICATOR_CODES:
            rows.append(
                {
                    "dataset_key": ds["dataset_key"],
                    "display_name": ds["display_name"],
                    "region": ds.get("region", "Global"),
                    "indicator_code": code,
                    "value": Decimal(ds["values"][code]),
                    "unit": _UNIT_BY_CODE[code],
                    "activity_unit": ds["activity_unit"],
                    "method_version": "EF 3.1",
                    "source": _SOURCE,
                    "year": _YEAR,
                    "notes": ds["basis"] + "." + _NOTE_SUFFIX,
                }
            )
    return rows


IMPACT_FACTORS = _expand()
